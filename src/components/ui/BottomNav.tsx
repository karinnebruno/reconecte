"use client";

import { motion } from "framer-motion";
import Link from "next/link";
import { usePathname } from "next/navigation";

const navItems = [
  { href: "/home",      label: "Início",    icon: HomeIcon },
  { href: "/trilhas",   label: "Desafios",  icon: TracksIcon },
  { href: "/progresso", label: "Progresso", icon: ProgressIcon },
  { href: "/agenda",    label: "Agenda",    icon: CalendarIcon },
];

export function BottomNav() {
  const pathname = usePathname();

  return (
    <nav className="fixed bottom-0 left-1/2 -translate-x-1/2 w-full max-w-[480px] md:max-w-[540px] z-50">
      <div className="bg-white border-t border-[#F0E2FB] px-2 pb-safe">
        <div className="flex items-center justify-around">
          {navItems.map(({ href, label, icon: Icon }) => {
            const active = pathname.startsWith(href);
            return (
              <Link
                key={href}
                href={href}
                className="flex flex-col items-center gap-0.5 py-3 px-4 relative"
              >
                {active && (
                  <motion.div
                    layoutId="nav-indicator"
                    className="absolute top-0 left-1/2 -translate-x-1/2 w-8 h-0.5 rounded-full bg-[#6B3FA0]"
                    transition={{ type: "spring", stiffness: 400, damping: 30 }}
                  />
                )}
                <Icon
                  className={`w-5 h-5 transition-colors duration-200 ${
                    active ? "text-[#6B3FA0]" : "text-[#9B7BB8]"
                  }`}
                />
                <span
                  className={`font-sans text-[10px] tracking-wide transition-colors duration-200 ${
                    active ? "text-[#6B3FA0]" : "text-[#9B7BB8]"
                  }`}
                >
                  {label}
                </span>
              </Link>
            );
          })}
        </div>
      </div>
    </nav>
  );
}

